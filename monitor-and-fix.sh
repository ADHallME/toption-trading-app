# Function to check if build is still running
check_build_status() {
    # We'll monitor for about 5 minutes (300 seconds)
    local timeout=300
    local elapsed=0
    local interval=30
    
    while [ $elapsed -lt $timeout ]; do
        echo "â³ Checking build status... (${elapsed}s elapsed)"
        
        # Try to curl the live site to see if it's updated
        if curl -s "https://www.toptiontrade.com/dashboard/main" | grep -q "Research Center\|Portfolio Analytics"; then
            echo "âœ… SUCCESS: Build deployed successfully!"
            echo "ğŸ‰ Premium tabs are now live at https://www.toptiontrade.com/dashboard/main"
            return 0
        fi
        
        sleep $interval
        elapsed=$((elapsed + interval))
    done
    
    echo "âš ï¸  Build taking longer than expected. Checking for common issues..."
    return 1
}

# Function to implement common fixes
apply_common_fixes() {
    echo "ğŸ”§ Applying common build fixes..."
    
    # Fix 1: Ensure all imports are properly typed
    echo "ğŸ”§ Fix 1: Checking TypeScript imports..."
    
    # Check if the issue is with React import
    if ! grep -q "import React" src/components/trading/index.tsx; then
        echo "ğŸ”§ Adding missing React import..."
        sed -i '1i import React from '\''react'\'';' src/components/trading/index.tsx
    fi
    
    # Fix 2: Ensure proper export syntax
    echo "ğŸ”§ Fix 2: Verifying export syntax..."
    
    # Fix 3: Check for missing dependencies
    echo "ğŸ”§ Fix 3: Checking package.json dependencies..."
    
    # Most common issue: JSX in wrong file type - already fixed
    echo "âœ… JSX file extension already corrected (.tsx)"
    
    # Fix 4: Ensure all required React imports
    echo "ğŸ”§ Fix 4: Ensuring proper React/JSX setup..."
    
    return 0
}

